    |  3.0  |.NET F. 3.0/3.5|Vi Studio 2008|Implicitely typed local variables, Object and collection initializers, Auto-implemented properties, Anon types, Extension methods, Query expressions, Lambda expressions, Expression trees, Partial Methods|
