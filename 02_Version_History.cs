    |  5.0  |.NET Frame. 4.5|Vi S 2012/2013|Async features, Caller information|
    |  6.0  |.NET Frame. 4.6|Vi S 2013/2015| Expression Bodied Methods, Auto-property initializer, nameof Expression, Primary constructor, Await in catch block, Exception filter, String interpolation|
    |  7.0  |.NET Core 2.0  |Visual S. 2017| out variables, Tuples, Discards, Pattern Matching, Local functions, Generalized async return types|
    |  8.0  |.NET Core 3.0  |Visual S. 2019|Readonly members, Default interface methods, Using declarations, Static local functions, Disposable ref structs, Nullable reference types|
